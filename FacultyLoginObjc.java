package com.example.nagar.onlinetest;

public class FacultyLoginObjc {
    String status,usns,passs;

    public FacultyLoginObjc(String status, String usns, String passs) {
        this.status = status;
        this.usns = usns;
        this.passs = passs;
    }

    public String getStatus() {
        return status;
    }

    public String getUsns() {
        return usns;
    }

    public String getPasss() {
        return passs;
    }
}
